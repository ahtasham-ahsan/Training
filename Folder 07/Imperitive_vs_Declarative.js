// Imperative vs Declarative Coding
// Imperative ==> tells the computer what to do and how to do

for (let index = 0; index < 1000; index++) {
    console.log(index);
  }
  
  // Declarative ==> tells the computer what to do and what should happen...
  [1,2,3].forEach(item => {
      console.log(item);
  })